/**
 * Fill every [data-i18n] / [data-i18n-ph] / [data-i18n-aria] node under `root` from
 * the current locale, and re-fill whenever the locale changes (cleaned up via signal).
 * @param {ParentNode} root
 * @param {AbortSignal} [signal]
 */
export function applyI18n(root, signal) {
  const render = () => {
    for (const el of root.querySelectorAll('[data-i18n]')) {
      el.innerHTML = i18n.t(/** @type {HTMLElement} */ (el).dataset.i18n);
    }
    for (const el of root.querySelectorAll('[data-i18n-ph]')) {
      el.setAttribute('placeholder', i18n.t(/** @type {HTMLElement} */ (el).dataset.i18nPh));
    }
    for (const el of root.querySelectorAll('[data-i18n-aria]')) {
      el.setAttribute('aria-label', i18n.t(/** @type {HTMLElement} */ (el).dataset.i18nAria));
    }
  };
  render();
  i18n.onChange(render, { signal });
}
